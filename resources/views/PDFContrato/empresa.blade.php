        <h3 style="text-align: center;">COMO CONTRATAR ESTAGIÁRIOS</h3>
        <div style="font-size: 12px;">
        <p><b>PODE CONTRATAR ESTAGIÁRIO:</b><br>
            &nbsp;&nbsp; Pessoa jurídica de direito privado, órgãos da administração pública direta, profissionais<br>
            &nbsp;&nbsp; liberais de nível superior, devidamente registrado nos seus conselhos de classe; Ex: (Advogados;<br>
            &nbsp;&nbsp; Dentistas; Engenheiros; Arquitetos; Contadores; Psicólogos; Administradores; e outros).</p>
        <p>
            <b>PROCEDIMENTO:</b><br>
            &nbsp;&nbsp;Formalizando o convênio com o ACERT ESTÁGIOS, conforme proposta em anexo, sem custo, através de: e-mail – telefone ou solicitando atendimento de um dos nossos profissionais.<br>
            <br>
            <b>BENEFÍCIOS PARA A EMPRESA:</b><br>
            &nbsp;&nbsp;A empresa não terá vínculo empregatício com os estagiários, não tem obrigatoriedade de pagar os demais encargos, tais como: 13° salário, FGTS, INSS e aviso prévio;<br>
            &nbsp;&nbsp;Estará desenvolvendo o aluno e visando à preparação para o trabalho produtivo do mesmo;<br>
            &nbsp;&nbsp;Estará formando conforme filosofia da empresa um futuro profissional.<br>
            <br>
            <b>TEMPO MÁXIMO QUE O MESMO ALUNO PODE PERMANECER COMO ESTAGIÁRIO NA MESMA EMPRESA:</b><br>
            &nbsp;&nbsp;Até dois anos. A empresa pode efetivar ou dispensar o estagiário a qualquer momento.<br>
            <br>
            <b>SOMENTE PODE SER ESTAGIÁRIO O ALUNO QUE:</b><br>
            &nbsp;&nbsp;Estiver devidamente matriculado e frequentando as aulas e que tenha acima de 16 anos e esteja a partir do: Ensino Médio; Técnico; Graduação; Pós - Graduação.<br>
            <br>
            <b>CARGA HORÁRIA PERMITIDA POR LEI (N°: 11.788/2008):</b><br>
            &nbsp;&nbsp;Até 06 horas diárias e 30 horas semanais.<br>
            <br>
            <b>DOCUMENTOS NECESSÁRIOS DO ALUNO:</b><br>
            &nbsp;&nbsp;Cadastro do ACERT ESTÁGIOS, (sem custo); RG, CPF e declaração da matrícula original;<br>
            <br>
            <b>CUSTOS PARA A EMPRESA:</b><br>
            <br>
        <p style="text-align: center;">PARA O ESTAGIÁRIO: é definido: <b>bolsa auxílio mensal</b> (salário),<b> auxílio-transporte e o recesso de 30 (trinta dias) remunerado</b> após um ano de estágio ou proporcional caso o mesmo seja desligado do estágio antes de um ano.<br>
            <b>&nbsp;O valor da bolsa auxilio e auxilio transporte serão definidos pela empresa.</b><br>
            &nbsp;&nbsp;Contribuição Institucional (CI) para o ACERT ESTÁGIOS:<br>
        </p>
        &nbsp;<b>●</b> O valor mínimo da Contribuição Institucional (CI) é de R$ 50,00 paga intermediação de estagiários de ensino médio.<br>
        &nbsp;<b>●</b> O valor mínimo da Contribuição Institucional (CI) é de R$ 75,00 paga intermediação de estagiários de ensino médio técnico e superior.<br>
        &nbsp;&nbsp;Os valores serão pagos em um único boleto bancário emitido pela empresa através do site www.acertestagios.com.br. (Incluso seguro de acidentes pessoais e morte/invalidez para o estagiário do primeiro ao último dia de contrato). NOSSOS SERVIÇOS SÃO AMPARADOS NA LEI N°11.788/2008.<br>
        &nbsp;&nbsp;O valor de bolsa assim como, auxilio transporte serão pagos diretamente pela empresa ao estagiário em conta bancário do titular ou caso de pagamento em espécie, envio de recibo assinado por ambas as partes.<br>
        <br>
        DIREITOS, DEVERES E OBRIGAÇÕES DE CADA PARTE<br>
        <br>
        <b>ESTAGIÁRIO:</b><br>
        <br>
        &nbsp;&nbsp;<b>•</b> Cumprir as cláusulas contratuais;<br>
        &nbsp;&nbsp;<b>•</b> Direito a bolsa-auxílio ou contraprestação; auxílio-transporte e recesso de 30 (trinta) dias remunerado após um ano de estágio, ou proporcional aos dias trabalhados;<br>
        &nbsp;&nbsp;<b>•</b> Facultativo contribuir como segurado do regime geral de previdência social;<br>
        &nbsp;&nbsp;<b>•</b> Responsável pela manutenção de sua conta bancária.<br>
        <br>
        <br>
        <b>INSTITUIÇÃO DE ENSINO:</b><br>
        <br>
        &nbsp;&nbsp;<b>•</b> Fiscalizar o estágio nas empresas;<br>
        &nbsp;&nbsp;<b>•</b> Determinar Normas e Diretrizes do Estágio;<br>
        &nbsp;&nbsp;<b>•</b> Cobrar a apresentação de relatórios de estágio;<br>
        &nbsp;&nbsp; <b>•</b> Comunicar a empresa o calendário de provas dos estagiários;<br>
        <br>
        <br>
        <b>PARTE CONCEDENTE DE ESTÁGIO (Empresa):</b><br>
        <br>
        &nbsp;&nbsp;<b>•</b> Ajustar o ambiente de estágio para o aluno;<br>
        &nbsp;&nbsp;<b>•</b> Indicar um funcionário do seu quadro de pessoal com formação ou experiência profissional na área para realizar a supervisão e orientação do estágio;<br>
        &nbsp;&nbsp;<b>•</b> Contratar em favor do estagiário seguro contra acidentes pessoais;<br>
        &nbsp;&nbsp;<b>•</b> Repassar conforme data acordada em convênio com o ACERT ESTÁGIOS, o valor da Bolsa - auxilio e Auxilio - transporte;<br>
        &nbsp;&nbsp;<b>•</b> Cumprir as cláusulas contratuais;<br>
        &nbsp;&nbsp;<b>•</b> Informar o início/término do estágio.<br>
        <br>
        <br>
        <b>ACERT ESTÁGIOS</b><br>
        <br>
        &nbsp;&nbsp;<b>•</b> Identificar oportunidades de estágio;<br>
        &nbsp;&nbsp;<b>•</b> Ajustar suas condições de realização;<br>
        &nbsp;&nbsp;<b>•</b> Fazer o acompanhamento administrativo;<br>
        &nbsp;&nbsp;<b>•</b> Encaminhar negociação de seguros contra acidentes pessoais;<br>
        &nbsp;&nbsp;<b>•</b> Cadastrar estudantes.<br>
        &nbsp;&nbsp;<b>•</b> Celebrar termo entre instituição, parte concedente e estudante.<br>
